"""Runtime evaluator for ``forms/<ID>/rubric.yaml`` checks.

Each form ships a ``rubric.yaml`` whose checks were authored as *prose* (plus
``depends_on_keys`` and a ``severity``). The prose is formulaic — it was
generated by one pipeline — so most checks compile to executable assertions:

- non-empty requirements (single key, key lists, ``a.{b,c}`` brace groups)
- date sanity (not in the future, on-or-before/after another date key)
- cross-field equality / must-differ checks
- "exactly one of" / "at most one of" enum selections (validated against the
  check's own value list or the form's ``schema.json`` enum)
- conditional requirements ("If X is true / = 'v' / is set, Y must ...")
- mutually-exclusive booleans
- statutory name-suffix requirements (quoted suffix lists)
- "not a P.O. Box" address checks
- format checks (email, single uppercase letter, contains a comma)
- fee totals (base + expedite premium) where the prose states one base amount
- at-least-one / exactly-one signer-block requirements
- paired name/address roster rows

A check whose prose does not compile is **never silently dropped**: it is
reported as a ``MANUAL_REVIEW`` issue carrying the original prose, so a human
(or agent) can still apply it.

Severity mapping: rubric ``required`` -> ``error``, ``optional`` ->
``warning``, ``advisory`` -> ``info``; non-compilable checks -> ``manual``.

Usage::

    from engine import rubric
    result = rubric.evaluate("LLC_MLLC-6", case)
    # {"ok": bool, "issues": [...], "stats": {...}}

    python -m engine.rubric <FORM_ID> <case.json> [forms_root]
    python -m engine.rubric --coverage [forms_root]   # compile-rate report
"""
import json
import re
import sys
from datetime import date, datetime
from pathlib import Path

from . import canonical
