# --------------------------------------------------------------------------
# severities
_SEV_MAP = {"required": "error", "optional": "warning", "advisory": "info"}

# dotted canonical key (optionally with [N] indexes)
_KEY = r"[A-Za-z_][A-Za-z0-9_]*(?:\[\d+\])?(?:\.[A-Za-z0-9_]+(?:\[\d+\])?)+"
_KEY_RE = re.compile(_KEY)
# brace group: filing.contact.{name,phone,email} or {name, phone, email}
_BRACE_RE = re.compile(r"(" + _KEY[:-1] + r"?)\.\{([^}]+)\}")

_MAINE = {"maine", "me"}
_POBOX_RE = re.compile(r"(?i)\b(p\.?\s*o\.?\s*box|post\s+office\s+box)")
_EMAIL_RE = re.compile(r"^[^@\s]+@[^@\s]+\.[^@\s]+$")

_EXPEDITE_PREMIUM = {
    None: 0, "": 0,
    "hold_for_pickup": 0,
    "24h_next_business_day": 50,
    "immediate_same_day": 100,
}

_DATE_FORMATS = (
    "%Y-%m-%d", "%m/%d/%Y", "%m/%d/%y", "%m-%d-%Y",
    "%B %d, %Y", "%b %d, %Y", "%d %B %Y", "%B %d %Y",
)


def _parse_date(value):
    """Parse a case date value to ``datetime.date`` or ``None``."""
    if isinstance(value, date):
        return value
    s = str(value or "").strip()
    if not s:
        return None
    for fmt in _DATE_FORMATS:
        try:
            return datetime.strptime(s, fmt).date()
        except ValueError:
            continue
    return None


def _is_empty(value):
    if value is None:
        return True
    if isinstance(value, str):
        return value.strip() == ""
    if isinstance(value, (list, tuple, set, dict)):
        return len(value) == 0
    return False


def _truthy(value):
    if isinstance(value, bool):
        return value
    if value is None:
        return False
    if isinstance(value, (int, float)):
        return value != 0
    s = str(value).strip().lower()
    if s in ("no", "false", "0", "off", "n", "", "none", "unchecked"):
        return False
    return bool(s)


def _norm(value):
    return str(value or "").strip().lower()


def _money(value):
    """Parse a fee value ('$135.00', 135, '135') to float, or None."""
    if value is None:
        return None
    if isinstance(value, (int, float)) and not isinstance(value, bool):
        return float(value)
    s = re.sub(r"[$,\s]", "", str(value))
    if not s:
        return None
    try:
        return float(s)
    except ValueError:
        return None


# --------------------------------------------------------------------------
# assertion primitives — each is fn(case, ctx) -> list of violation dicts
# ctx carries {"today": date}. Violations: {"code", "keys", "detail"}.

def _v(code, keys, detail):
    if isinstance(keys, str):
        keys = [keys]
    return {"code": code, "keys": list(keys), "detail": detail}


def a_nonempty(keys):
    def check(case, ctx):
        out = []
        for k in keys:
            if _is_empty(canonical.get(case, k)):
                out.append(_v("MISSING_REQUIRED", k, f"{k} is empty"))
        return out
    return check


def a_at_least_one(keys):
    def check(case, ctx):
        if any(not _is_empty(canonical.get(case, k)) for k in keys):
            return []
        return [_v("AT_LEAST_ONE_REQUIRED", keys,
                   "none of the alternatives is populated: "
                   + ", ".join(keys))]
    return check


def a_group_choice(groups, exactly_one):
    """At-least-one (or exactly-one) of several all-populated key groups."""
    def check(case, ctx):
        filled = [g for g in groups
                  if all(not _is_empty(canonical.get(case, k)) for k in g)]
        partial = [g for g in groups
                   if any(not _is_empty(canonical.get(case, k)) for k in g)]
        allkeys = [k for g in groups for k in g]
        if not filled:
            return [_v("SIGNER_BLOCK_REQUIRED", allkeys,
                       "no complete block is populated among: "
                       + " | ".join("+".join(g) for g in groups))]
        if exactly_one and len(partial) > 1:
            return [_v("MULTIPLE_BLOCKS_POPULATED", allkeys,
                       "more than one mutually-exclusive block is populated")]
        return []
    return check


def a_not_future(key, ref_key=None):
    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            return []
        d = _parse_date(val)
        if d is None:
            return [_v("UNPARSEABLE_DATE", key,
                       f"{key} = {val!r} is not a recognizable date")]
        if ref_key:
            rv = canonical.get(case, ref_key)
            if _is_empty(rv):
                return []
            rd = _parse_date(rv)
            if rd is None:
                return []
            if d > rd:
                return [_v("DATE_IN_FUTURE", [key, ref_key],
                           f"{key} ({d}) is after {ref_key} ({rd})")]
            return []
        if d > ctx["today"]:
            return [_v("DATE_IN_FUTURE", key,
                       f"{key} ({d}) is in the future")]
        return []
    return check


def a_date_order(key_a, op, key_b):
    """op: '<=', '>=', '<' — assert date(key_a) op date(key_b)."""
    def check(case, ctx):
        va, vb = canonical.get(case, key_a), canonical.get(case, key_b)
        if _is_empty(va) or _is_empty(vb):
            return []
        da, db = _parse_date(va), _parse_date(vb)
        out = []
        if da is None:
            out.append(_v("UNPARSEABLE_DATE", key_a,
                          f"{key_a} = {va!r} is not a recognizable date"))
        if db is None:
            out.append(_v("UNPARSEABLE_DATE", key_b,
                          f"{key_b} = {vb!r} is not a recognizable date"))
        if out or da is None or db is None:
            return out
        ok = {"<=": da <= db, ">=": da >= db, "<": da < db}[op]
        if not ok:
            word = {"<=": "on or before", ">=": "on or after",
                    "<": "strictly before"}[op]
            return [_v("DATE_ORDER", [key_a, key_b],
                       f"{key_a} ({da}) must be {word} {key_b} ({db})")]
        return []
    return check


def a_equals(key_a, key_b, alt_key=None):
    def check(case, ctx):
        va, vb = canonical.get(case, key_a), canonical.get(case, key_b)
        if _is_empty(va) or _is_empty(vb):
            return []
        if _norm(va) == _norm(vb):
            return []
        if alt_key is not None:
            valt = canonical.get(case, alt_key)
            if not _is_empty(valt) and _norm(va) == _norm(valt):
                return []
        return [_v("VALUE_MISMATCH", [key_a, key_b],
                   f"{key_a} ({va!r}) does not match {key_b} ({vb!r})")]
    return check


def a_differs(key_a, key_bs):
    def check(case, ctx):
        va = canonical.get(case, key_a)
        if _is_empty(va):
            return []
        out = []
        for kb in key_bs:
            vb = canonical.get(case, kb)
            if _is_empty(vb):
                continue
            if _norm(va) == _norm(vb):
                out.append(_v("VALUES_MUST_DIFFER", [key_a, kb],
                              f"{key_a} must differ from {kb} "
                              f"(both are {va!r})"))
        return out
    return check


def a_not_in(key, forbidden, require_nonempty=False):
    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            if require_nonempty:
                return [_v("MISSING_REQUIRED", key, f"{key} is empty")]
            return []
        if _norm(val) in forbidden:
            return [_v("FORBIDDEN_VALUE", key,
                       f"{key} must not be {val!r}")]
        return []
    return check


def a_one_of(key, allowed, required):
    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            if required:
                opts = (" to one of: " + ", ".join(sorted(allowed))
                        if allowed else " (exactly one option)")
                return [_v("CHOICE_REQUIRED", key,
                           f"{key} must be set" + opts)]
            return []
        if allowed and _norm(val) not in {a.lower() for a in allowed}:
            return [_v("ENUM_INVALID", key,
                       f"{key} = {val!r} is not one of: "
                       + ", ".join(sorted(allowed)))]
        return []
    return check


def a_mutex_bool(key_a, key_b):
    def check(case, ctx):
        if _truthy(canonical.get(case, key_a)) and \
                _truthy(canonical.get(case, key_b)):
            return [_v("MUTUALLY_EXCLUSIVE", [key_a, key_b],
                       f"{key_a} and {key_b} cannot both be true")]
        return []
    return check


def a_not_pobox(key, require_nonempty=False):
    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            if require_nonempty:
                return [_v("MISSING_REQUIRED", key, f"{key} is empty")]
            return []
        if _POBOX_RE.search(str(val)):
            return [_v("PO_BOX_FORBIDDEN", key,
                       f"{key} must not be a P.O. Box (got {val!r})")]
        return []
    return check


def a_contains_one_of(key, substrings, extra=None, skip_values=(),
                      anchored=False):
    """``key`` (when set) must contain one of ``substrings`` (case-insens.).

    ``extra``: (gate_key, more_substrings) — the extra suffixes also satisfy
    the check, but only when the gate key is truthy.

    ``anchored``: when True, the match is anchored to the END of the value
    (after stripping trailing whitespace/punctuation) rather than a
    match-anywhere substring test. This is the correct semantics for
    statutory entity-name *suffix* requirements: a plain substring test
    false-positives on names that merely embed a 2-char token — e.g. "LC"
    inside "Malcolm Holdings" or "Falcon Realty" would otherwise satisfy an
    LLC-suffix check. End-anchoring rejects those while still accepting
    "Acme LLC", "Acme L.L.C.", and "Acme, Inc.".
    """
    def _matches(low, sub):
        sub = sub.lower()
        if not anchored:
            return sub in low
        # Anchor to the end of the name. Strip trailing whitespace and
        # punctuation from the *name* so "Acme LLC." matches suffix "LLC",
        # while the suffix keeps its own trailing '.' (e.g. "Inc.") so both
        # "Acme, Inc." and "Acme Inc" are accepted.
        tail = low.rstrip(" \t.,;")
        sub_bare = sub.rstrip(" \t.,;")
        return tail.endswith(sub) or tail.endswith(sub_bare)

    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            return []
        low = _norm(val)
        if low in {s.lower() for s in skip_values}:
            return []
        allowed = list(substrings)
        if extra is not None:
            gate_key, more = extra
            if _truthy(canonical.get(case, gate_key)):
                allowed = allowed + list(more)
        if any(_matches(low, s) for s in allowed):
            return []
        return [_v("NAME_AFFIX_REQUIRED", key,
                   f"{key} = {val!r} must "
                   + ("end with" if anchored else "contain")
                   + " one of: "
                   + ", ".join(substrings)
                   + (f" (or, when {extra[0]} is true: "
                      + ", ".join(extra[1]) + ")" if extra else ""))]
    return check


def a_format(key, kind):
    """kind: 'email' | 'single_uppercase_letter' | 'contains_comma'."""
    def check(case, ctx):
        val = canonical.get(case, key)
        if _is_empty(val):
            return []
        s = str(val).strip()
        ok = {"email": bool(_EMAIL_RE.match(s)),
              "single_uppercase_letter": bool(re.fullmatch(r"[A-Z]", s)),
              "contains_comma": "," in s,
              "positive_integer": bool(re.fullmatch(r"[1-9]\d*", s))}[kind]
        if not ok:
            return [_v("FORMAT_INVALID", key,
                       f"{key} = {val!r} fails format check ({kind})")]
        return []
    return check


def a_fee(total_key, expedite_key, base, minimum=False):
    def check(case, ctx):
        total = _money(canonical.get(case, total_key))
        if total is None:
            return []  # fee total not asserted in the case — nothing to check
        tier = canonical.get(case, expedite_key) if expedite_key else None
        tier_s = _norm(tier) if tier is not None else ""
        if tier_s not in {_norm(k) for k in _EXPEDITE_PREMIUM if k}.union({""}):
            return [_v("UNKNOWN_EXPEDITE_TIER", expedite_key or total_key,
                       f"{expedite_key} = {tier!r} is not a known tier")]
        premium = _EXPEDITE_PREMIUM.get(tier_s or None, 0)
        expected = base + premium
        if minimum:
            if total < expected:
                return [_v("FEE_MISMATCH", total_key,
                           f"{total_key} (${total:g}) is below the "
                           f"${expected:g} minimum (${base:g} base"
                           + (f" + ${premium:g} expedite" if premium else "")
                           + ")")]
            return []
        if abs(total - expected) > 0.005:
            return [_v("FEE_MISMATCH", total_key,
                       f"{total_key} (${total:g}) != expected "
                       f"${expected:g} (${base:g} base"
                       + (f" + ${premium:g} expedite" if premium else "")
                       + ")")]
        return []
    return check


def a_conditional(predicate, then_assertions, pred_text):
    def check(case, ctx):
        if not predicate(case):
            return []
        out = []
        for sub in then_assertions:
            for viol in sub(case, ctx):
                viol = dict(viol)
                viol["code"] = ("CONDITIONAL_FORBIDDEN"
                                if viol["code"] == "MUST_BE_ABSENT"
                                else "CONDITIONAL_MISSING"
                                if viol["code"] in ("MISSING_REQUIRED",
                                                    "CHOICE_REQUIRED")
                                else viol["code"])
                viol["detail"] = f"when {pred_text}: " + viol["detail"]
                out.append(viol)
        return out
    return check


def a_absent(keys):
    def check(case, ctx):
        out = []
        for k in keys:
            if not _is_empty(canonical.get(case, k)):
                out.append(_v("MUST_BE_ABSENT", k,
                              f"{k} must be empty"))
        return out
    return check


def a_truthy(keys):
    def check(case, ctx):
        out = []
        for k in keys:
            if not _truthy(canonical.get(case, k)):
                out.append(_v("MISSING_REQUIRED", k,
                              f"{k} must be true"))
        return out
    return check


def a_both_or_neither(key_a, key_b):
    def check(case, ctx):
        ea, eb = (_is_empty(canonical.get(case, key_a)),
                  _is_empty(canonical.get(case, key_b)))
        if ea != eb:
            return [_v("PAIRED_FIELDS_INCOMPLETE", [key_a, key_b],
                       f"{key_a} and {key_b} must be populated together "
                       "(or both left empty)")]
        return []
    return check


def a_name_address_pairs(pairs):
    def check(case, ctx):
        out = []
        for name_key, addr_key in pairs:
            if not _is_empty(canonical.get(case, name_key)) and \
                    _is_empty(canonical.get(case, addr_key)):
                out.append(_v("PAIRED_FIELDS_INCOMPLETE",
                              [name_key, addr_key],
                              f"{name_key} is populated but {addr_key} "
                              "is empty"))
        return out
    return check


