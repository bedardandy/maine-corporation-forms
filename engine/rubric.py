# loading & evaluation

def load_rubric(form_id, forms_root="forms"):
    path = Path(forms_root) / form_id / "rubric.yaml"
    if not path.exists():
        return {"checks": []}
    import yaml
    return yaml.safe_load(path.read_text(encoding="utf-8")) or {"checks": []}


def _schema_enums(form_id, forms_root):
    """Map dotted canonical keys -> enum value sets.

    Sources: ``schema.json`` enums, overlaid by ``mapping.json`` option keys
    (the mapping is the verified artifact, so its options win).
    """
    out = {}
    path = Path(forms_root) / form_id / "schema.json"
    if path.exists():
        try:
            schema = json.loads(path.read_text(encoding="utf-8"))
        except Exception:
            schema = {}

        def walk(node, prefix):
            for k, v in (node.get("properties") or {}).items():
                if not isinstance(v, dict):
                    continue
                dotted = f"{prefix}.{k}" if prefix else k
                if v.get("enum"):
                    out[dotted] = set(str(x) for x in v["enum"])
                walk(v, dotted)
                items = v.get("items")
                if isinstance(items, dict):
                    walk(items, dotted + "[0]")
        walk(schema, "")
    mpath = Path(forms_root) / form_id / "mapping.json"
    if mpath.exists():
        try:
            mapping = json.loads(mpath.read_text(encoding="utf-8"))
            fields = mapping_entries(mapping)
        except Exception:
            fields = {}
        for key, spec in fields.items():
            if not isinstance(spec, dict):
                continue
            if spec.get("field_type") in ("radio", "enum_select",
                                          "enum_text_select"):
                opts = spec.get("options")
                if isinstance(opts, dict) and opts:
                    out[spec.get("canonical_key", key)] = set(opts)
    return out


def evaluate(form_id, case, forms_root="forms", today=None):
    """Evaluate ``form_id``'s rubric checks against ``case``.

    Returns ``{"ok", "form_id", "issues", "stats"}``. ``issues`` is a list of
    ``{code, severity, rubric_severity, check_id, keys, message,
    rule_source}``; ``MANUAL_REVIEW`` entries carry the original prose for
    checks that need human judgment. ``ok`` is False only when an
    error-severity issue is present.
    """
    rubric = load_rubric(form_id, forms_root)
    checks = rubric.get("checks") or []
    enums = _schema_enums(form_id, forms_root)
    ctx = {"today": today or date.today()}
    rule_src = f"forms/{form_id}/rubric.yaml"

    issues = []
    n_machine = n_manual = 0
    for check in checks:
        cid = check.get("id") or "unnamed-check"
        rub_sev = (check.get("severity") or "required").lower()
        sev = _SEV_MAP.get(rub_sev, "error")
        assertions = compile_check(check, enums)
        if assertions is None:
            n_manual += 1
            issues.append({
                "code": "MANUAL_REVIEW",
                "severity": "manual",
                "rubric_severity": rub_sev,
                "check_id": cid,
                "keys": list(check.get("depends_on_keys") or []),
                "message": (check.get("description") or "").strip(),
                "rule_source": f"{rule_src}#{cid}",
            })
            continue
        n_machine += 1
        for assertion in assertions:
            for viol in assertion(case, ctx):
                v_sev = "warning" if viol["code"] == "UNPARSEABLE_DATE" \
                    else sev
                issues.append({
                    "code": viol["code"],
                    "severity": v_sev,
                    "rubric_severity": rub_sev,
                    "check_id": cid,
                    "keys": viol["keys"],
                    "message": viol["detail"],
                    "rule_source": f"{rule_src}#{cid}",
                })

    return {
        "ok": not any(i["severity"] == "error" for i in issues),
        "form_id": form_id,
        "issues": issues,
        "stats": {
            "checks": len(checks),
            "machine_evaluated": n_machine,
            "manual_review": n_manual,
        },
    }


def coverage(forms_root="forms"):
    """Compile-rate report across every form's rubric (no case needed)."""
    root = Path(forms_root)
    total = machine = 0
    per_form = {}
    unmatched = []
    for d in sorted(p for p in root.iterdir() if p.is_dir()):
        path = d / "rubric.yaml"
        if not path.exists():
            continue
        rubric = load_rubric(d.name, forms_root)
        enums = _schema_enums(d.name, forms_root)
        m = t = 0
        for check in rubric.get("checks") or []:
            t += 1
            if compile_check(check, enums) is not None:
                m += 1
            else:
                unmatched.append((d.name, check.get("id"),
                                  (check.get("description") or "").strip()))
        total += t
        machine += m
        per_form[d.name] = (m, t)
    return {"total_checks": total, "machine_evaluated": machine,
            "manual_review": total - machine, "per_form": per_form,
            "unmatched": unmatched}


def _cli(argv):
    if "--coverage" in argv:
        forms_root = next((a for a in argv[1:] if not a.startswith("--")),
                          "forms")
        cov = coverage(forms_root)
        pct = 100.0 * cov["machine_evaluated"] / max(cov["total_checks"], 1)
        print(f"{cov['machine_evaluated']}/{cov['total_checks']} checks "
              f"machine-evaluated ({pct:.1f}%); "
              f"{cov['manual_review']} manual-review")
        if "--unmatched" in argv:
            for fid, cid, desc in cov["unmatched"]:
                print(f"  {fid}#{cid}: {desc[:120]}")
        return 0
    if len(argv) < 3:
        print("usage: python -m engine.rubric <FORM_ID> <case.json> "
              "[forms_root] | --coverage [forms_root] [--unmatched]")
        return 1
    form_id = argv[1]
    case = json.loads(Path(argv[2]).read_text(encoding="utf-8"))
    forms_root = argv[3] if len(argv) > 3 else "forms"
    result = evaluate(form_id, case, forms_root)
    print(json.dumps(result, indent=2))
    return 0 if result["ok"] else 1


if __name__ == "__main__":
    sys.exit(_cli(sys.argv))
