# prose -> assertion compiler

def _expand_braces(text):
    """Expand ``a.b.{x,y,z}`` (and ``a.{x, y}``) into ``a.b.x, a.b.y, ...``."""
    def repl(m):
        prefix = m.group(1)
        parts = [p.strip() for p in m.group(2).split(",")]
        return ", ".join(f"{prefix}.{p}" for p in parts if p)
    return _BRACE_RE.sub(repl, text)


def _quoted(text):
    """All single-quoted strings in ``text`` (smart quotes tolerated)."""
    return re.findall(r"[‘']([^'’]+)[’']", text)


def _enum_tokens(text):
    """Quoted strings that look like enum tokens (snake_case identifiers).

    Filters prose quotes like 'X one box only' out of option lists.
    """
    return {t for t in _quoted(text) if re.fullmatch(r"[a-z0-9_]+", t)}


def _strip_parens(text):
    """Drop parentheticals that carry no dotted keys or dollar amounts.

    Quoted-suffix and enum parentheticals are consumed from the *raw*
    description by their dedicated handlers before this runs, so plain
    commentary parens (statute cites, 'Shape D', 'must match SOS record')
    can be dropped safely. Runs to fixpoint to unwrap nesting.
    """
    def repl(m):
        inner = m.group(1)
        if _KEY_RE.search(inner) or "$" in inner \
                or "vice versa" in inner or "if low-profit" in inner:
            return m.group(0)
        return ""
    prev = None
    out = text
    while out != prev:
        prev = out
        out = re.sub(r"\(([^()]*)\)", repl, out)
    out = re.sub(r"\s+", " ", out)
    # tighten dangling space before sentence punctuation, but never glue a
    # relative ".key" fragment onto the previous word ("street and .city")
    out = re.sub(r"\s+([.,;])(?=\s|$)", r"\1", out)
    return out.strip()


def _parse_condition(text, carried_key=None):
    """Parse a condition clause -> (predicate, key, text) or None."""
    text = text.strip()
    if re.search(r"\b\w+_N\b", text):
        return None  # ungrounded row template ("partner_entity_N.name")
    m = re.match(rf"^({_KEY})\s+is\s+true$", text)
    if m:
        k = m.group(1)
        return (lambda c, k=k: _truthy(canonical.get(c, k))), k, f"{k} is true"
    m = re.match(rf"^({_KEY})\s+is\s+false$", text)
    if m:
        k = m.group(1)
        return ((lambda c, k=k: not _truthy(canonical.get(c, k))), k,
                f"{k} is false")
    m = re.match(rf"^({_KEY})\s*=\s*'([^']+)'$", text)
    if m:
        k, v = m.group(1), m.group(2)
        return ((lambda c, k=k, v=v: _norm(canonical.get(c, k)) == v.lower()),
                k, f"{k} = '{v}'")
    # elliptical: "if 'noncommercial'" / "If false" — reuse the carried key
    m = re.match(r"^'([^']+)'$", text)
    if m and carried_key:
        k, v = carried_key, m.group(1)
        return ((lambda c, k=k, v=v: _norm(canonical.get(c, k)) == v.lower()),
                k, f"{k} = '{v}'")
    if text == "false" and carried_key:
        k = carried_key
        return ((lambda c, k=k: not _truthy(canonical.get(c, k))), k,
                f"{k} is false")
    if text == "true" and carried_key:
        k = carried_key
        return ((lambda c, k=k: _truthy(canonical.get(c, k))), k,
                f"{k} is true")
    m = re.match(rf"^({_KEY})\s+is\s+(?:set|populated|non-empty)$", text)
    if m:
        k = m.group(1)
        return ((lambda c, k=k: not _is_empty(canonical.get(c, k))), k,
                f"{k} is set")
    m = re.match(rf"^({_KEY})\s+is\s+not\s+'([^']+)'(?:\s*/\s*'([^']+)')?$",
                 text)
    if m:
        k = m.group(1)
        vals = {m.group(2).lower()}
        if m.group(3):
            vals.add(m.group(3).lower())
        def pred(c, k=k, vals=vals):
            v = canonical.get(c, k)
            return not _is_empty(v) and _norm(v) not in vals
        return pred, k, f"{k} is not " + "/".join(sorted(vals))
    m = re.match(rf"^({_KEY})\s+differs\s+from\s+({_KEY})$", text)
    if m:
        ka, kb = m.group(1), m.group(2)
        def pred(c, ka=ka, kb=kb):
            va, vb = canonical.get(c, ka), canonical.get(c, kb)
            return (not _is_empty(va) and not _is_empty(vb)
                    and _norm(va) != _norm(vb))
        return pred, ka, f"{ka} differs from {kb}"
    return None


def _name_address_pairs_from_keys(keys):
    names = [k for k in keys if k.endswith(".name")]
    pairs = [(n, n[: -len(".name")] + ".address") for n in names
             if n[: -len(".name")] + ".address" in keys]
    return pairs


def _parse_consequence(text, cond_key=None, carried_subject=None,
                       check_keys=()):
    """Parse a conditional consequence -> (assertions, subject) or None."""
    text = text.strip().rstrip(".;").strip()

    # special: roster-pair consequence ("all populated X rows must have both
    # name and address") — pairs are recovered from depends_on_keys
    if re.search(r"all populated \S+ rows must have both name and address",
                 text, re.I):
        pairs = _name_address_pairs_from_keys(check_keys)
        if pairs:
            return [a_name_address_pairs(pairs)], None
        return None
    if re.search(r"\b\w+_N\b", text):
        return None  # ungrounded row template — needs human review
    if re.search(r"\bexactly one\b.*\boptions? (?:is|must be) selected",
                 text, re.I):
        keys_in = [k for k in _KEY_RE.findall(text) if "_N" not in k]
        target = keys_in[0] if keys_in else None
        if target:
            return [a_one_of(target, set(), required=True)], target
        return None

    # split off the subject(s)
    text = re.sub(r"^at least\s+", "", text)
    m = re.match(rf"^({_KEY})(?: [Aa][Nn][Dd] ({_KEY}))?\s+(.*)$", text)
    if m:
        subjects = [m.group(1)] + ([m.group(2)] if m.group(2) else [])
        pred = m.group(3)
    elif re.match(r"^it\s+", text):
        subject = carried_subject or cond_key
        if subject is None:
            return None
        subjects = [subject]
        pred = text[3:].strip()
    else:
        return None

    subject = subjects[0]
    if re.search(rf"\(or {_KEY} when (?:set|populated)\)", pred):
        return None  # alternate-subject fallback — keep for human review
    vice_versa = "vice versa" in pred
    pred = re.sub(r"\(?and vice versa\)?", "", pred).strip()

    if re.match(r"^(?:must (?:also |both |all )?(?:be )?|(?:is|are) "
                r"(?:both )?)(?:non-empty|set|populated)$", pred):
        if vice_versa and cond_key and len(subjects) == 1:
            return [a_both_or_neither(cond_key, subject)], subject
        return [a_nonempty(subjects)], subject
    if re.match(r"^must (?:also )?be true$", pred):
        return [a_truthy(subjects)], subject
    if re.match(r"^(?:must|should) (?:also )?be (?:absent|empty|left empty)$",
                pred):
        return [a_absent(subjects)], subject
    if re.match(r"^must not be a P\.?O\.? Box$", pred, re.I):
        return [a_not_pobox(subject)], subject
    if "must look like a valid email" in pred:
        return [a_format(subject, "email")], subject
    if "single uppercase letter" in pred:
        return [a_format(subject, "single_uppercase_letter")], subject
    m = re.match(rf"^(?:must be|is|should be) on or after ({_KEY})$", pred)
    if m:
        return [a_date_order(subject, ">=", m.group(1))], subject
    m = re.match(rf"^(?:must be|is|should be) on or before ({_KEY})$", pred)
    if m:
        return [a_date_order(subject, "<=", m.group(1))], subject
    if "must contain one of" in pred:
        quoted = _quoted(pred)
        if quoted:
            # entity-name suffix requirement: anchor to end of name so a
            # 2-char token ('LP', 'LC') doesn't match mid-name.
            return [a_contains_one_of(subject, quoted, anchored=True)], subject
    m = re.match(rf"^must not equal '([^']+)'(?: or '([^']+)')?$", pred)
    if m:
        vals = {m.group(1).lower()}
        if m.group(2):
            vals.add(m.group(2).lower())
        return [a_not_in(subject, vals)], subject
    return None


def _compile_conditional(desc, schema_enums, check_keys=()):
    """Compile one-or-more "If <cond>, <consequence>" sentences, or None."""
    sentences = re.split(r"(?<=[.;])\s+(?=[Ii]f\b)", desc)
    # also split a single sentence on '; if'
    parts = []
    for s in sentences:
        parts.extend(re.split(r";\s*(?=if\b)", s, flags=re.I))
    assertions = []
    carried_key = None
    carried_subject = None
    matched_any = False
    noop = re.compile(r"not required|is also valid|no meeting was held|"
                      r"need not", re.I)
    for part in parts:
        part = part.strip()
        if not part:
            continue
        m = re.match(r"^[Ii]f\s+(.+?),\s*(.+)$", part)
        if not m:
            # trailing non-conditional sentence: tolerate pure no-ops
            # ("...is not required." / "Zero is also valid...")
            if matched_any and noop.search(part):
                continue
            return None
        cond = _parse_condition(m.group(1).strip(), carried_key)
        if cond is None:
            if matched_any and noop.search(part):
                continue
            return None
        predicate, cond_key, pred_text = cond
        cons = _parse_consequence(m.group(2), cond_key, carried_subject,
                                  check_keys)
        if cons is None:
            if matched_any and noop.search(part):
                continue
            return None
        carried_key = cond_key
        sub_assertions, subject = cons
        if subject:
            carried_subject = subject
        assertions.append(a_conditional(predicate, sub_assertions, pred_text))
        matched_any = True
    return assertions or None


def _enum_for(key, schema_enums):
    return schema_enums.get(key, set())


def compile_check(check, schema_enums=None):
    """Compile one rubric check to a list of assertions, or ``None``.

    ``schema_enums`` maps dotted canonical keys to their schema enum values
    (used to validate "exactly one of ... options is selected" checks).
    """
    schema_enums = schema_enums or {}
    raw = (check.get("description") or "").strip()
    keys = list(check.get("depends_on_keys") or [])
    if not raw:
        return None
    desc = _expand_braces(re.sub(r"\s+", " ", raw))

    # ---- fee checks (before parenthetical stripping; need the $ text) ----
    if "total_fees_dollars" in desc:
        total_key = next((k for k in keys if "total_fees" in k),
                         "filing.total_fees_dollars")
        exp_key = next((k for k in keys if "expedited" in k),
                       "filing.expedited_service")
        # base fee depending on home jurisdiction is still machine-checkable
        m = re.search(rf"equals the base fee \(\$(\d+) if ({_KEY}) is "
                      rf"Maine/ME, else \$(\d+)\)", desc)
        if m:
            dom, jkey, foreign = (float(m.group(1)), m.group(2),
                                  float(m.group(3)))

            def fee_by_jurisdiction(case, ctx, total_key=total_key,
                                    exp_key=exp_key, jkey=jkey, dom=dom,
                                    foreign=foreign):
                j = _norm(canonical.get(case, jkey))
                if not j:
                    return []
                base = dom if j in _MAINE else foreign
                return a_fee(total_key, exp_key, base)(case, ctx)
            return [fee_by_jurisdiction]
        if re.search(r"bundled|accompanying-form|entity.type|entity type|"
                     r"month|\bor \$", desc, re.I):
            return None  # genuinely needs facts not in the case
        if re.search(r"No Filing Fee", desc):
            return [a_fee(total_key, exp_key, 0.0)]
        m = re.search(r">=\s*\$(\d+(?:\.\d+)?)", desc)
        if m:
            return [a_fee(total_key, exp_key, float(m.group(1)),
                          minimum=True)]
        m = re.search(r"is approximately \$(\d+(?:\.\d+)?)", desc)
        if m:
            return [a_fee(total_key, exp_key, float(m.group(1)),
                          minimum=True)]
        amounts = {float(a) for a in re.findall(r"\$(\d+(?:\.\d+)?)", desc)
                   if float(a) not in (50.0, 100.0, 0.0)}
        m = re.search(r"equals (?:the )?\$(\d+(?:\.\d+)?)", desc)
        if m and len(amounts) <= 1:
            return [a_fee(total_key, exp_key, float(m.group(1)))]
        return None

    # ---- LLC-style suffix list with a low-profit extension ----
    if re.search(r"ends with one of|must contain one of|contains a statutory",
                 desc) and "If " not in desc[:3]:
        if "abbreviation" in desc:
            return None  # open-ended suffix list — cannot enumerate
        m = re.match(rf"^({_KEY})", desc)
        if m and _quoted(desc):
            key = m.group(1)
            lp = re.search(r"\(if low-profit\)(.*?)(?:\(|$)", desc)
            if lp:
                extra_sufs = _quoted(lp.group(1))
                base_sufs = [s for s in _quoted(desc.split("(if low-profit)")[0])]
                gate = next((k for k in keys if "low_profit" in k), None)
                if gate and base_sufs and extra_sufs:
                    return [a_contains_one_of(key, base_sufs,
                                              extra=(gate, extra_sufs),
                                              anchored=True)]
            return [a_contains_one_of(key, _quoted(desc), anchored=True)]
        return None

    stripped = _strip_parens(desc)

    # ---- mutually exclusive booleans ----
    m = re.match(rf"^({_KEY}) and ({_KEY}) cannot both be true\.?$", stripped)
    if m:
        return [a_mutex_bool(m.group(1), m.group(2))]

    # ---- conditional sentences ----
    if re.match(r"^[Ii]f\b", stripped):
        # conditional suffix requirement keeps quoting from the raw desc
        m = re.match(rf"^If ({_KEY}) is populated and not '([^']+)', it must "
                     rf"contain one of (.+)$", desc)
        if m:
            quoted = _quoted(m.group(3))
            if quoted:
                return [a_contains_one_of(m.group(1), quoted,
                                          skip_values=[m.group(2)],
                                          anchored=True)]
        out = _compile_conditional(desc, schema_enums, keys)
        if out:
            return out
        out = _compile_conditional(stripped, schema_enums, keys)
        if out:
            return out
        return None

    # ---- "Exactly one <key> option / enum value is selected" ----
    m = re.match(rf"^Exactly one ({_KEY}) (?:option|enum value|value)s? is "
                 rf"selected", stripped)
    if m:
        target = m.group(1)
        # only an authoritative enum (schema/mapping) gates the value;
        # prose-quoted tokens are shorthand and can disagree with the data
        return [a_one_of(target, _enum_for(target, schema_enums),
                         required=True)]

    # ---- exactly-one / at-most-one selections (single enum key) ----
    if re.search(r"^(?:Exactly|At most) one of\b", stripped) or \
            re.search(r"is (?:set to )?exactly one of\b", stripped):
        target = None
        m = re.search(rf"\(?({_KEY})\s*(?:∈|in)\s*\{{([^}}]+)\}}", desc)
        allowed = set()
        if m:
            target = m.group(1)
            allowed = set(_quoted("'" + m.group(2).replace("'", "'") + "'")
                          or _quoted(m.group(2)))
            if not allowed:
                allowed = {t.strip().strip("'‘’")
                           for t in m.group(2).split(",") if t.strip()}
        if target is None:
            m = re.match(rf"^({_KEY}) is (?:set to )?exactly one of (.+)$",
                         stripped)
            if m:
                target = m.group(1)
                rest = m.group(2)
                allowed = set(_quoted(rest)) or \
                    {t.strip().strip(".'")
                     for t in rest.split("|") if t.strip()}
        if target is None and len(keys) == 1:
            target = keys[0]
            # the schema/mapping enum is authoritative; prose quotes are
            # shorthand and can disagree with the data model
            allowed = _enum_for(target, schema_enums)
        if target is None:
            return None
        if not allowed:
            allowed = _enum_for(target, schema_enums)
        required = bool(re.search(r"^Exactly one|is (?:set to )?exactly one",
                                  stripped)) and \
            not re.search(r"[Aa]t most one|[Zz]ero is also valid", desc)
        return [a_one_of(target, allowed, required=required)]

    # ---- signer blocks / either-or groups ----
    m = re.match(
        rf"^Exactly one signer block is populated: either ({_KEY})\s*"
        rf"OR \(?({_KEY}) AND ({_KEY})\)?", stripped)
    if m:
        return [a_group_choice([[m.group(1)], [m.group(2), m.group(3)]],
                               exactly_one=True)]
    m = re.match(
        rf"^At least one signer present: ({_KEY}) OR "
        rf"\(?({_KEY}) \+ ({_KEY})\)?", stripped)
    if m:
        return [a_group_choice([[m.group(1)], [m.group(2), m.group(3)]],
                               exactly_one=False)]
    m = re.match(
        rf"^Either \(?({_KEY})\)? is non-empty[^,.]*,? ?(?:or|OR) "
        rf"\(?({_KEY}) (?:AND|and) ({_KEY})\)?[^.]*are both non-empty",
        stripped)
    if m:
        return [a_group_choice([[m.group(1)], [m.group(2), m.group(3)]],
                               exactly_one=False)]

    # ---- at least one of ... is non-empty ----
    if re.match(r"^At least one of\b.*\bis non-empty", stripped) and keys:
        return [a_at_least_one(keys)]

    # ---- "All <N> required-role slots are populated: k1, k2, ..." ----
    m = re.match(r"^All .*? are populated: (.+)$", stripped)
    if m:
        listed = _KEY_RE.findall(m.group(1))
        if listed:
            return [a_nonempty(listed)]

    # ---- officer/director name+address pairing ----
    if re.match(r"^Every populated \S+ has a corresponding", stripped):
        names = [k for k in keys if k.endswith(".name")]
        pairs = [(n, n[: -len(".name")] + ".address") for n in names
                 if n[: -len(".name")] + ".address" in keys]
        if pairs and len(pairs) == len(names):
            return [a_name_address_pairs(pairs)]
        return None

    # ---- both-or-neither blocks ----
    m = re.match(rf"^.*either both ({_KEY}) [Aa][Nn][Dd] ({_KEY}) are set, "
                 rf"or both are empty", stripped)
    if m:
        return [a_both_or_neither(m.group(1), m.group(2))]
    m = re.match(rf"^({_KEY}) and ({_KEY}) are either both populated"
                 rf".*or both empty", stripped)
    if m:
        return [a_both_or_neither(m.group(1), m.group(2))]

    # ---- declarative clause chains: "K is non-empty and ... and ..." ----
    return _compile_declarative(stripped, desc, keys)


def _compile_declarative(stripped, raw_desc, keys):
    """Compile "X [, Y and Z] is/are <predicate> [and <predicate>...]"."""
    # multi-key non-empty: "a, b, and c are (all) non-empty"
    m = re.match(
        rf"^(?:Both )?((?:{_KEY})(?:,? (?:and )?{_KEY})*) are (?:all |both )?"
        rf"non-empty\.?$", stripped)
    if m:
        return [a_nonempty(_KEY_RE.findall(m.group(1)))]

    m = re.match(rf"^(?:Both )?({_KEY}) and ({_KEY}) are not '[^']+'",
                 stripped)
    if m and re.search(r"'Maine'|'ME'", stripped):
        return [a_not_in(m.group(1), _MAINE), a_not_in(m.group(2), _MAINE)]

    m = re.match(rf"^({_KEY})(?:\s*\([^)]*\))?\s+(is|must be|must not be|"
                 rf"does not|matches|equals|differs|is not|should|parses)"
                 rf"\b(.*)$", stripped)
    if not m:
        return None
    key = m.group(1)
    rest = (m.group(2) + m.group(3)).strip()

    assertions = []
    # split the predicate chain conservatively
    clauses = re.split(r"\s+and\s+(?=is\b|not\b|on\b|matches\b|equals\b|"
                       r"contains\b|does\b|from\b|parses\b|should\b|a\b)"
                       r"|;\s*", rest)
    for clause in clauses:
        clause = clause.strip().rstrip(".").strip()
        if not clause:
            continue
        assertion = _match_clause(key, clause)
        if assertion is None:
            # retry without trailing commentary: parentheticals first, then
            # an em-dash tail ("... — cannot terminate before formation")
            bare = re.sub(r"\([^()]*\)", "", clause)
            bare = re.sub(r"\s+", " ", bare).strip().rstrip(".").strip()
            assertion = _match_clause(key, bare)
            if assertion is None and "—" in bare:
                assertion = _match_clause(
                    key, bare.split("—")[0].strip().rstrip(".").strip())
        if assertion is None:
            return None  # unrecognized clause — the whole check stays manual
        assertions.append(assertion)
    return assertions or None


def _match_clause(key, clause):
    """Match one declarative predicate clause for ``key``; None if unknown."""
    if not clause:
        return None
    if re.match(r"^(?:is |must be )?non-?empty$", clause):
        return a_nonempty([key])
    if re.match(r"^is true$", clause):
        return a_truthy([key])
    if re.match(r"^(?:is )?not in the future(?: relative to (?:the "
                rf"submission date|today|{_KEY}))?$", clause):
        mref = re.search(rf"relative to ({_KEY})", clause)
        return a_not_future(key, mref.group(1) if mref else None)
    if re.match(r"^(?:is )?on or before today$", clause):
        return a_not_future(key)
    mm = re.match(rf"^(?:is )?on or before ({_KEY})$", clause)
    if mm:
        return a_date_order(key, "<=", mm.group(1))
    mm = re.match(rf"^(?:is )?on or after ({_KEY})$", clause)
    if mm:
        return a_date_order(key, ">=", mm.group(1))
    mm = re.match(rf"^(?:is )?strictly before ({_KEY})$", clause)
    if mm:
        return a_date_order(key, "<", mm.group(1))
    mm = re.match(rf"^parses as a valid date in the past relative to "
                  rf"({_KEY})$", clause)
    if mm:
        return a_date_order(key, "<=", mm.group(1))
    mm = re.match(rf"^(?:should )?(?:match(?:es)?|equal(?:s)?|"
                  rf"is equal to) ({_KEY})"
                  rf"(?: \(or ({_KEY}) when populated\))?$", clause)
    if mm:
        return a_equals(key, mm.group(1), mm.group(2))
    mm = re.match(rf"^(?:differs from|does not equal|is different from) "
                  rf"(?:both )?({_KEY})(?: and(?: from)? ({_KEY}))?$",
                  clause)
    if mm:
        targets = [mm.group(1)] + ([mm.group(2)] if mm.group(2) else [])
        return a_differs(key, targets)
    if re.match(r"^(?:is |must )?not (?:be )?a P\.?O\.? Box$", clause,
                re.I) or \
            re.match(r"^(?:must not|does not) begin with 'P\.?O\.?'",
                     clause, re.I):
        return a_not_pobox(key)
    if re.match(r"^(?:is )?(?:NOT|not) 'Maine'(?: or 'ME'|\s*/\s*'ME')?$",
                clause):
        return a_not_in(key, _MAINE)
    if re.match(r"^contains a comma$", clause):
        return a_format(key, "contains_comma")
    if re.match(r"^(?:is )?a positive integer$", clause):
        return a_format(key, "positive_integer")
    mm = re.match(r"^should be set to one of \{(.+?)\}", clause)
    if mm:
        vals = set(_quoted(mm.group(1))) or \
            {t.strip().strip("'") for t in mm.group(1).split(",")}
        return a_one_of(key, vals, required=False)
    return None


